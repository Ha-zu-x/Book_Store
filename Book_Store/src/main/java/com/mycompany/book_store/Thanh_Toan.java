    void Search() {
        try {
            String keyword = txtSearch.getText();
            Sach sa = sdao.selectedByKeyword1(keyword);
            KhuyenMai km = kdao.selectedByKeyword1(keyword);
            txtBookName.setText(sa.getTenSach());
            txtPrice.setText(String.valueOf(sa.getGiaTien())); 
            if ( km == null ) {
                txtPercent.setText("0");
            } else {
                Date curentDate = new Date();
                Date kmDate = km.getNgayKetThuc();
                if(kmDate != null && kmDate.after(curentDate)){
                    txtPercent.setText(String.valueOf(km.getPhanTramGiam()));
                } else {                  
                        txtPercent.setText("0"); 
                }

            } 
        } catch (Exception e) {
            MgsBox.alert(this,  "Sai mã sách!");
        }
    }
    
     @Override
    public void run() {
        while (true) {
            try {
                Date now = new Date();
                SimpleDateFormat timeFormater = new SimpleDateFormat("hh:mm:ss aa");
                SimpleDateFormat dateFormater = new SimpleDateFormat("dd:MM:yyyy");
                lblTime.setText(timeFormater.format(now));
                lblDate.setText(dateFormater.format(now));

                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                break;
            }
        }
    }

