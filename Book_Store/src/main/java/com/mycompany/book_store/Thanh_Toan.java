    void addList(){
        Double actualPrice = Double.parseDouble(txtPrice.getText())*(1 - Double.parseDouble(txtPercent.getText())/100)* Double.parseDouble(txtValue.getText());
        String search = txtSearch.getText();
        String bookName = txtBookName.getText();
        String value = txtValue.getText(); 
        String price = String.valueOf(actualPrice); 

        // Create a new row and add it to the table
        Object[] row = {search, bookName, value, price};
        DefaultTableModel model = (DefaultTableModel) lblBill.getModel(); 
        model.addRow(row);
 
        // Add the row to the array2D as well
        ArrayList<String> newRow = new ArrayList<>();
        newRow.add(search);
        newRow.add(bookName);
        newRow.add(value);
        newRow.add(price);   
        array2D.add(newRow);
    } 
    
